// lecture: 4.1 - For Loop
// for loop for table no. 3 from 1 to 10

public class Loops1 {
    public static void main(String[] args) {
        int n = 3;
        for (int i = 1; i <= 10; i++) {
            System.out.println(n + " * " + i + " = " + n * i);
        }
    }
}

// Output:  (Table of 3)
// 3 * 1 = 3
// 3 * 2 = 6
// 3 * 3 = 9
// 3 * 4 = 12
// 3 * 5 = 15
// 3 * 6 = 18
// 3 * 7 = 21
// 3 * 8 = 24
// 3 * 9 = 27
// 3 * 10 = 30
