// Lecture: 8.6 - Loops
// Write a infinite loop using do while loop.

public class Loops3 {
    public static void main(String[] args) {
        do {
            System.out.println("This is an infinite loop");
        } while (true);
    }
}


// Output:
// This is an infinite loop
// This is an infinite loop
// This is an infinite loop
// This is an infinite loop
// This is an infinite loop
// This is an infinite loop


// Explanation:
// In this program, we have created an infinite loop using the do-while loop. The condition in the do-while loop is always true, so the loop will continue to execute indefinitely. The statement inside the loop will be printed continuously.